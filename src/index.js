/**
 * Returns all states as an array of { abbr, name } objects
 */
export function getAll() {
  return states;
}

/**
 * Returns an array of two-letter state abbreviations
 */
export function getAbbreviations() {
  return states.map((s) => s.abbr);
}

/**
 * Returns an array of full state names
 */
export function getNames() {
  return states.map((s) => s.name);
}

/**
 * Looks up a state by abbreviation (case-insensitive)
 * @param {string} abbr
 * @returns {{ abbr: string, name: string } | undefined}
 */
export function findByAbbr(abbr) {
  return states.find((s) => s.abbr === abbr.toUpperCase());
}

/**
 * Looks up a state by full name (case-insensitive)
 * @param {string} name
 * @returns {{ abbr: string, name: string } | undefined}
 */
export function findByName(name) {
  return states.find((s) => s.name.toLowerCase() === name.toLowerCase());
}
